
public class forLoop7 
{

	public static void main(String[] args)
	{
		
		for (int i = 1; i < 4; i++) 
		{	
			int j = i;
			switch (j) {
			case 1:
				System.out.println("1111");
				break;
			case 2:
				System.out.println("2222");
				break;	
			case 3:
				System.out.println("3333");
				break;	
			
			default:
				System.out.print("Error");
				
			}
			
		}
	
	
	}

	
}
		

