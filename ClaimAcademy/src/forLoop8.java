
public class forLoop8 
{

	public static void main(String[] args)
	{
		
		for (int i = 1; i < 5; i++) 
		{	
			int j = i;
			switch (j) {
			case 1:
				System.out.println("1");
				break;
			case 2:
				System.out.println("22");
				break;	
			case 3:
				System.out.println("333");
				break;	
			case 4:
				System.out.println("4444");
				break;
			default:
				System.out.print("Error");
				
			}
			
		}
	
	
	}

	
}
		

